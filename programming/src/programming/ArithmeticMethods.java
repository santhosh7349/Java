package programming;

public class ArithmeticMethods {
	public int add(int a,int b)
	{
		return a+b;
	}
	public int sub(int b,int a)
	{
		return b-a;
	}
	public int mul(int a,int b)
	{
		return a*b;
	}
	public int div(int b,int a)
	{
		return b/a;
	}
	public int rem(int b,int a)
	{
		return b%a;
	}
}
